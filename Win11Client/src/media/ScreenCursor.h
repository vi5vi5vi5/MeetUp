#pragma once
#include <QImage>
#include <QPoint>
#include <QRect>

class QScreen;

// Курсор мыши для демонстрации экрана.
//
// Захват экрана в Qt на Windows идёт через Desktop Duplication, а он отдаёт
// кадр БЕЗ курсора (позиция и картинка курсора приходят отдельным каналом,
// которым Qt не пользуется). Захват окна идёт другим путём и курсор рисует
// сам — отсюда и разница, которую видно глазами. Значит для экрана курсор
// дорисовываем мы.
//
// Всё нужное (GetCursorInfo, DrawIconEx) работает с любого потока, поэтому
// снимок делается прямо на потоке кодирования — курсор оказывается даже
// свежее кадра, а не отстаёт от него.
struct ScreenCursor {
    QImage image;        // ARGB32, пусто — курсора нет (скрыт, не удалось снять)
    QPoint hotspot;      // точка «острия» внутри картинки, пиксели картинки
    QPoint posPhysical;  // положение острия на виртуальном рабочем столе, физ. пиксели

    bool isNull() const { return image.isNull(); }

    // Снять текущий курсор. Картинки кэшируются по хендлу: форма меняется
    // редко (стрелка/текст/ожидание), а рисовать иконку каждый кадр незачем.
    static ScreenCursor grab();

    // Физический прямоугольник монитора, соответствующего этому QScreen.
    // Нужен, чтобы перевести положение курсора из координат рабочего стола в
    // координаты кадра. Пусто — не нашли.
    static QRect monitorRect(const QScreen* screen);
};
